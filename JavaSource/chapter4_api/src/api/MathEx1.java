package api;

public class MathEx1 {
	public static void main(String[] args) {
		double val = 90.7552;
		
		// 반올림 ( Math.round() )
		System.out.println("반올림 : " + Math.round(val));

		// 올림 ( Math.ceil() )
		System.out.println("올림 : " + Math.ceil(val));
		
		// 내림 ( Math.floor() )
		System.out.println("내림 : " + Math.round(val));
		
		// 랜덤 ( Math.random() )
		System.out.println("랜덤 : " + Math.random());
	}
}
