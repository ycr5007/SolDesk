package op;

public class IncreaseDecreaseOperEx1 {
	public static void main(String[] args) {
		int i = 10;
		
		System.out.println("i : " + i);
		++i; // i = i + 1;
		System.out.println("++i : " + i);
		i++;
		System.out.println("i++ : " + i);
		
		--i; // i = i - 1;
		System.out.println("--i : " + i);
		i--;
		System.out.println("i-- : " + i);
	}
}
