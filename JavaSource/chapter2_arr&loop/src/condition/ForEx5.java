public class ForEx5 {
	public static void main(String[] args) {
		for(int i = 1; i < 10; i++) {
			System.out.print("3 * " + i + " = " + (3 * i) + "\t");
		}
		
		System.out.println();
		
		for(int i = 1; i < 10; i++) {
			System.out.println("2 * " + i + " = " + (2 * i));
		}
	}
}
